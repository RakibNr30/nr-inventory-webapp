<?php

namespace Modules\Cms\Services;

use Modules\Cms\Repositories\CampaignRepository;

class CampaignService
{
    /**
     * @var $brandRepository
     */
    protected $brandRepository;

    /**
     * Constructor
     *
     * @param CampaignRepository $brandRepository
     */
    public function __construct(CampaignRepository $brandRepository)
    {
        $this->brandRepository = $brandRepository;
    }

    /**
     * Get all data
     *
     * @param int $limit
     * @return mixed
     */
    public function all($limit = 0)
    {
        return $this->brandRepository->paginate($limit);
    }

    /**
     * Get all data
     *
     * @param $data
     * @return mixed
     */
    public function create($data)
    {
        return $this->brandRepository->create($data);
    }

    /**
     * Find data
     *
     * @param $id
     * @return mixed
     */
    public function find($id)
    {
        return $this->brandRepository->find($id);
    }

    /**
     * Update data
     *
     * @param $data
     * @param $id
     * @return mixed
     */
    public function update($data, $id)
    {
        return $this->brandRepository->update($data, $id);
    }

    /**
     * Delete data
     *
     * @param $id
     * @return mixed
     */
    public function delete($id)
    {
        return $this->brandRepository->delete($id);
    }

    /**
     * Find data
     *
     * @param $attribute
     * @param $value
     * @return mixed
     */
    public function findBy($attribute, $value)
    {
        return $this->brandRepository->findBy($attribute, $value);
    }
}
